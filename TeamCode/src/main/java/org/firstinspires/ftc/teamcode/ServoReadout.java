@Config
@Autonomous(group = "drive")
public class ServoReadout extends LinearOpMode {

    private Servo purplePixelServo;

    @Override
    public void runOpMode() throws InterruptedException {
        Telemetry telemetry = new MultipleTelemetry(this.telemetry, FtcDashboard.getInstance().getTelemetry());

        purplePixelServo = hardwareMap.get(Servo.class, "purple_pixel_servo");
        /* Color Sensor Setup End */


        waitForStart();
        purplePixelServo.setPosition(0);
        float position = 0;

        while (opModeIsActive()){
            purplePixelServo.setPosition((gamepad1.left_stick_x + 1)/2.0);
            telemetry.addData("Servo at ", purplePixelServo.getPosition());
            telemetry.update();
        }

    }
}